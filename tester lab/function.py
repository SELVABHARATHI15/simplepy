#1.with argument with return
def greet(name):
    return "Hello "+name
print(greet("selva"))
#2.with argument without return
def greet(name):
    print("Hello "+name)
greet("selva")
#3.without argument with return
def greet():
    return "Hello sir"
print(greet())
#4.without argument without return
def greet():
    print("Hello sir")
greet()
